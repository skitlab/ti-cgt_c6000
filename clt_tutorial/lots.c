/*****************************************************************************/
/* LOTS.C - defines lots() and static function local() for Cache Layout      */
/*          Tool Tutorial.                                                   */
/*****************************************************************************/
#include <stdio.h>
static void local();

/*****************************************************************************/
/* LOTS() - calls lots.c:local() 100+ times.                                 */
/*****************************************************************************/
void lots(int i)
{
   local();
   if (i % 3 == 0) local();
}

/*****************************************************************************/
/* LOCAL() - print out a '.' to stdout.                                      */
/*****************************************************************************/
void local() 
{
   printf("."); 
}
